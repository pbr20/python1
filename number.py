print(math.gcd(12, 18))  # greatest common divisor
print(math.lcm(4, 6))  # least common multiple
print(math.sqrt(16))  # square root
print(math.isqrt(27))  # integer square root
print(math.factorial(5))  # factorial
print(math.comb(5, 2))  # combination
print(math.perm(5, 2))  # permutation
print(math.ceil(2.9))  # ceiling
print(math.floor(2.9))  # floor
print(math.log2(8))  # logarithm base 2
print(math.pi)  # pi
print(math.sin(math.pi/2))  # sine
print(math.cos(math.pi))  # cosine
print(math.tan(math.pi/4))  # tangent

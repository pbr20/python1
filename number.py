import math

x = 1
x = 1.1
x = 1 + 2j  # complex number
print(type(x))
print(x)
print(x.real)
print(x.imag)
print(x.conjugate())

print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)  # float division
print(10 // 3)  # floor division
print(10 % 3)  # modulus
print(10 ** 3)  # exponentiation

print(round(2.9))
print(abs(-2.9))
print(pow(2, 3))  # 2 raised to the power of 3
print(max(2, 3, 4, 5, 6))
print(min(2, 3, 4, 5, 6))
print(sum([2, 3, 4, 5, 6]))

print(2+3*4)  # operator precedence
print((2+3)*4)  # operator precedence with parentheses

